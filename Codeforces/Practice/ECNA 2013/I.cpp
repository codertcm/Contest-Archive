void rest(int n, ull x){
	x--;
	while (n--)
		putchar((!!(x & (1ULL<<n))) + 'a');
}

void word(int n, ull x){
	if (n == 0) return;

	if (x <= (1ULL<<(n-1))){
		printf("a");
		rest(n-1, x);
	} else {
		printf("bb");
		word(n-2, x - (1ULL<<(n-1)));
	}

}

void start(ull x){
	int i;
	for (i = 1; prefix[i] < x; i++);
	word(i, x - prefix[i-1]);
}

int main(){
	int i, t = 0;

	while (scanf("%d %llu", &n, &m), t++, (n || m)){
		printf("Case %d: ", t);
		start(n*(m-1) + 1);
		printf(" ");
		start(n*m);
		printf("\n");
	}
}
